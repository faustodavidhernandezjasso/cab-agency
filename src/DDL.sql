create table cliente(
	idCliente SERIAL NOT NULL UNIQUE,
	nombre VARCHAR(50) NOT NULL CHECK (nombre <> ''),
	apellidoPaterno VARCHAR(50) NOT NULL CHECK (apellidoPaterno <> ''),
	apellidoMaterno VARCHAR(50) CHECK (apellidoMaterno <> ''),
